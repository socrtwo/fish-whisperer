/** Labels for CLIP zero-shot classification. Includes non-fish categories for detection. */
export function getCandidateLabels(): string[] {
  const fishLabels = fishDatabase.map(
    (fish) => `a ${fish.commonName.toLowerCase()}`
  );
  const nonFishLabels = [
    'a photo with no fish',
    'a person with no fish',
    'a boat with no fish',
    'a scenic water landscape with no fish',
  ];
  return [...fishLabels, ...nonFishLabels];
}

/** Maps a CLIP label back to its FishSpecies entry. Returns undefined for non-fish labels. */
export function findFishByLabel(label: string): FishSpecies | undefined {
  const cleaned = label.replace(/^a\s+/, '').toLowerCase();
  return fishDatabase.find(
    (fish) => fish.commonName.toLowerCase() === cleaned
  );
}
